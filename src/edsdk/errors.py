class EdsError(Exception):
    """Exception raised when an EDSDK call returns a non-OK error code."""

    def __init__(self, code: int, context: str | None = None) -> None:
        self.code = code
        self.name = _ERROR_NAMES.get(code, f"EDS_ERR_0x{code:08X}")
        self.context = context
        msg = f"{self.name} (0x{code:08X})"
        if context:
            msg = f"{context}: {msg}"
        super().__init__(msg)


def check(code: int, context: str | None = None) -> None:
    """Raise :class:`EdsError` when ``code`` is non-zero."""
    if code != EDS_ERR_OK:
        raise EdsError(code, context)
